class AdditionalCriteria extends Component<any, any> {
  state = {
    popupInd: false,
    show: false,
  };

  handleOnClick = () => {
    this.setState({ popupInd: true });
  };

  onClose = () => {
    this.setState({ popupInd: false });
    return true;
  };

  processCloseActions = () => {
    this.setState({ show: true });
  };

  render() {
    const { active } = this.props;

    return (
      <Fragment>
        {active ? (
          <div
            className="additional-criteria-active"
            onClick={this.handleOnClick}
          >
            <YesIcon />
            <span>Yes</span>
          </div>
        ) : (
          <div className="additional-criteria-disable">
            <NoIcon />
            <span>No</span>
          </div>
        )}
        <DialogPopup
          className="additional-criteria-modal"
          showCloseIcon={false}
          positiveActionText=""
          negativeActionText=""
          title="additional criteria"
          handleClose={() => {
            this.onClose();
          }}
          handleAction={() => {
            this.processCloseActions();
          }}
          showActions={false}
          open={this.state.popupInd}
        >
          <AdditionalCriteriaModal />
        </DialogPopup>
      </Fragment>
    );
  }
}

export default AdditionalCriteria;
